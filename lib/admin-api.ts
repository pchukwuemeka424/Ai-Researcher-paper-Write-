export function exportSessionTopicsCsv(sessions: RecentSessionTopic[]): void {
	const headers = [
		"Topic",
		"Lecture",
		"Generated by",
		"Email",
		"Workflow",
		"Model",
		"State",
		"Messages",
		"Created",
		"Updated",
	];
	const rows = sessions.map((session) => [
		session.topic,
		session.lectureTitle ?? "",
		session.generatedByName ?? "",
		session.generatedByEmail ?? "",
		session.workflow ?? "",
		session.model,
		session.state,
		String(session.messageCount),
		session.createdAt,
		session.updatedAt,
	]);
	const csv = [headers, ...rows]
		.map((row) => row.map((cell) => `"${String(cell).replace(/"/g, '""')}"`).join(","))
		.join("\n");
	const blob = new Blob([csv], { type: "text/csv;charset=utf-8;" });
	const url = URL.createObjectURL(blob);
	const link = document.createElement("a");
	link.href = url;
	link.download = `aula-sessions-${new Date().toISOString().slice(0, 10)}.csv`;
	link.click();
	URL.revokeObjectURL(url);
}

